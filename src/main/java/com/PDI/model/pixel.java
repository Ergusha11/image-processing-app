package com.PDI.model;

public class pixel{
    private double red;
	private double green;
	private double blue;

	public pixel(){
		this.red = 0;
		this.green = 0;
		this.blue = 0;
	}

	public double getRed() {
		return red;
	}

	public void setRed(double red) {
		this.red = red;
	}

	public double getGreen() {
		return green;
	}

	public void setGreen(double green) {
		this.green = green;
	}

	public double getBlue() {
		return blue;
	}

	public void setBlue(double blue) {
		this.blue = blue;
	}
}
